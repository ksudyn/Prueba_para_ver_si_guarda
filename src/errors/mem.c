#include "../../inc/miniRT.h"

void	free_arr(char **arr)
{
	int		i;

	if (!arr)
		return ;
	i = 0;
	while (arr && arr[i])
	{
		free(arr[i]);
		i++;
	}
	if (arr)
		free(arr);
}
