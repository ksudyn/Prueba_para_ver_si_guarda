#include "libft.h"

char	*ft_strnchr(char *s, int c, int len)
{
	int	i;

	i = 0;
	while (s[i] != (char)c && i < len)
	{
		if (s[i] == '\0')
			return (NULL);
		i++;
	}
	return (s + i);
}
