#include "miniRT.h"

void	free_arg(char **arg)
{
	int		i;

	if (!arg)
		return ;
	i = 0;
	while (arg && arg[i])
	{
		free(arg[i]);
		i++;
	}
	if (arg)
		free(arg);
}