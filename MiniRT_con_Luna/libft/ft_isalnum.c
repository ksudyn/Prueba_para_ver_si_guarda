int	ft_isalnum(char c)
{
	int	isalpha;
	int	isnum;

	isalpha = ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A'));
	isnum = (c <= '9' && c >= '0');
	return (isalpha || isnum);
}
